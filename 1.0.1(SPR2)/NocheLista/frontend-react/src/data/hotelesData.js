export function getHotelData(id) {
  return hotelesData[id] || null;
}

const STOP_WORDS = new Set(['hotel', 'la', 'el', 'los', 'las', 'de', 'del', 'san', 'santa', 'don', 'doña', 'y', 'e', 'en']);

function wordOverlap(a, b) {
  const wordsA = a.toLowerCase().split(/\s+/).filter(w => !STOP_WORDS.has(w));
  const wordsB = b.toLowerCase().split(/\s+/).filter(w => !STOP_WORDS.has(w));
  if (!wordsA.length || !wordsB.length) return false;
  const common = wordsA.filter(w => wordsB.includes(w));
  const shorterLen = Math.min(wordsA.length, wordsB.length);
  return common.length >= shorterLen && common.length >= 1;
}

export function getHotelDataByName(nombre) {
  if (!nombre) return null;
  const entry = Object.values(hotelesData).find((h) => {
    if (h.nombre.toLowerCase() === nombre.toLowerCase()) return true;
    const a = h.nombre.toLowerCase().trim();
    const b = nombre.toLowerCase().trim();
    if (a.includes(b) || b.includes(a)) return true;
    if (wordOverlap(a, b)) return true;
    return false;
  });
  return entry || null;
}

export function getHotelDataByApiData(apiData) {
  if (!apiData) return null;
  if (apiData.id && hotelesData[apiData.id]) return hotelesData[apiData.id];
  if (apiData.nombre) return getHotelDataByName(apiData.nombre);
  return null;
}

export function getAllHotelesLocal() {
  return Object.values(hotelesData);
}

export function mergeWithApiData(localData, apiData = {}) {
  if (!localData) {
    return {
      ...apiData,
      imagen_url: apiData.imagen_url || apiData.imagen_principal || '/principal.avif',
    };
  }
  return {
    ...apiData,
    ...localData,
    id: apiData.id || localData.id,
    direccion: apiData.direccion || localData.direccion,
    habitaciones: apiData.habitaciones || localData.habitaciones || [],
    imagen_url: localData.imagen_url || apiData.imagen_url || apiData.imagen_principal || '/principal.avif',
    nombre: localData.nombre || apiData.nombre,
  };
}

